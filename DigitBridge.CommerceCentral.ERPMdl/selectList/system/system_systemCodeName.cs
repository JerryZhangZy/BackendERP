{
 
    public partial class system_systemCodeName : SelectListBase
    {
        public override string Name => "system_systemCodeName";

        public system_systemCodeName(IDataBaseFactory dbFactory) : base(dbFactory) { }

        protected override void SetFilterSqlString()
        {
            this.QueryObject.LoadAll = false;
            if (!string.IsNullOrEmpty(this.QueryObject.Term.FilterValue))
                this.QueryObject.SetTermSqlString(
                    $"COALESCE(tbl.SystemCodeName, '') LIKE '{this.QueryObject.Term.FilterValue.ToSqlSafeString()}%' "
                );
            else
                this.QueryObject.SetTermSqlString(null);
        }

        protected override string GetSQL_select()
        {
            this.SetFilterSqlString();
            this.SQL_Select = $@"
 
    SELECT 
      tbl.SystemCodeName AS [value],
      tbl.Description AS[text],
      1 AS [count] FROM
      [dbo].[SystemCodes] tbl
   
    WHERE COALESCE(tbl.SystemCodeName,'') != '' 
        AND {this.QueryObject.GetSQL()}
ORDER BY tbl.SystemCodeName 
";
            return this.SQL_Select;
        }
    }
