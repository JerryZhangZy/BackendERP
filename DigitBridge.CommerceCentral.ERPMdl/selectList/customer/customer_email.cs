namespace DigitBridge.CommerceCentral.ERPMdl.selectList.customer
{
 
    public partial class customer_email : SelectListBase
    {
        public override string Name => "customer_email";

        public customer_email(IDataBaseFactory dbFactory) : base(dbFactory) { }

        protected override void SetFilterSqlString()
        {
            this.QueryObject.LoadAll = false;
            if (!string.IsNullOrEmpty(this.QueryObject.Term.FilterValue))
                this.QueryObject.SetTermSqlString(
                    $"tbl.Email LIKE '{this.QueryObject.Term.FilterValue.ToSqlSafeString()}%' "
                );
            else
                this.QueryObject.SetTermSqlString(null);
        }

        protected override string GetSQL_select()
        {
            this.SetFilterSqlString();
            this.SQL_Select = $@"
 
    SELECT 
      tbl.Email AS [value],
      '' AS[text],
        COUNT(1) AS [count] FROM
      [dbo].[Customer] tbl
   
    WHERE tbl.Email != '' 
        AND {this.QueryObject.GetSQL()}
    GROUP BY tbl.Email
ORDER BY tbl.Email 
";
            return this.SQL_Select;
        }
    }
}
